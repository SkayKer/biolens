  /// Supprime la plante et retourne √† l'herbier.
  Future<void> _deletePlant() async {
    if (_plant == null) return;

    final colorScheme = Theme.of(context).colorScheme;

    try {
      // Supprimer l'image locale si elle existe
      final imageFile = File(_plant!.imagePath);
      if (await imageFile.exists()) {
        await imageFile.delete();
      }

      // Supprimer de la base de donn√©es
      await _storageService.deletePlant(_plant!.id);

      if (mounted) {
        // Afficher un message de confirmation
        ScaffoldMessenger.of(context).showSnackBar(
          SnackBar(
            content: Text('${_plant!.commonName} a √©t√© supprim√©'),
            backgroundColor: colorScheme.primary,
          ),
        );

        // Retourner √† l'herbier
        context.go('/herbier');
      }
    } catch (e) {
      if (mounted) {
        ScaffoldMessenger.of(context).showSnackBar(
          SnackBar(
            content: Text('Erreur lors de la suppression: $e'),
            backgroundColor: colorScheme.error,
          ),
        );
      }
    }
  }

